static uint32_t subw(uint32_t x) {
  return ((uint32_t)sbox[(x >> 24) & 0xFF] << 24) |
         ((uint32_t)sbox[(x >> 16) & 0xFF] << 16) |
         ((uint32_t)sbox[(x >> 8) & 0xFF] << 8) | ((uint32_t)sbox[x & 0xFF]);
}
static uint32_t rotw(uint32_t x) { return (x << 8) | (x >> 24); }
static uint32_t rcon(int n) {
  uint8_t r = 1;
  for (int i = 1; i < n; i++) {
    r = (uint8_t)((r << 1) ^ ((r & 0x80) ? 0x1B : 0x00));
  }
  return (uint32_t)r << 24;
}
static void key_expansion(const uint8_t key[32], uint32_t* rk) {
  int i = 0;
  for (; i < 8; i++)
    rk[i] = ((uint32_t)key[4 * i] << 24) | ((uint32_t)key[4 * i + 1] << 16) |
            ((uint32_t)key[4 * i + 2] << 8) | ((uint32_t)key[4 * i + 3]);
  for (; i < 60; i++) {
    uint32_t t = rk[i - 1];
    if (i % 8 == 0) {
      t = subw(rotw(t)) ^ rcon(i / 8);
    } else if (i % 8 == 4) {
      t = subw(t);
    }
    rk[i] = rk[i - 8] ^ t;
  }
}
static uint8_t xtime(uint8_t x) {
  return (uint8_t)((x << 1) ^ ((x & 0x80) ? 0x1B : 0x00));
}
static uint8_t mul(uint8_t a, uint8_t b) {
  uint8_t r = 0;
  while (b) {
    if (b & 1) r ^= a;
    a = xtime(a);
    b >>= 1;
  }
  return r;
}
static void addroundkey(uint8_t s[16], const uint32_t* rk) {
  for (int i = 0; i < 4; i++) {
    uint32_t k = rk[i];
    s[4 * i] ^= (uint8_t)(k >> 24);
    s[4 * i + 1] ^= (uint8_t)(k >> 16);
    s[4 * i + 2] ^= (uint8_t)(k >> 8);
    s[4 * i + 3] ^= (uint8_t)k;
  }
}
static void subbytes(uint8_t s[16]) {
  for (int i = 0; i < 16; i++) s[i] = sbox[s[i]];
}
static void shiftrows(uint8_t s[16]) {
  uint8_t t[16];
  t[0] = s[0];
  t[1] = s[5];
  t[2] = s[10];
  t[3] = s[15];
  t[4] = s[4];
  t[5] = s[9];
  t[6] = s[14];
  t[7] = s[3];
  t[8] = s[8];
  t[9] = s[13];
  t[10] = s[2];
  t[11] = s[7];
  t[12] = s[12];
  t[13] = s[1];
  t[14] = s[6];
  t[15] = s[11];
  memcpy(s, t, 16);
}
static void mixcolumns(uint8_t s[16]) {
  for (int i = 0; i < 4; i++) {
    uint8_t* c = &s[4 * i];
    uint8_t a0 = c[0], a1 = c[1], a2 = c[2], a3 = c[3];
    c[0] = mul(2, a0) ^ mul(3, a1) ^ a2 ^ a3;
    c[1] = a0 ^ mul(2, a1) ^ mul(3, a2) ^ a3;
    c[2] = a0 ^ a1 ^ mul(2, a2) ^ mul(3, a3);
    c[3] = mul(3, a0) ^ a1 ^ a2 ^ mul(2, a3);
  }
}
static void aes_encrypt_block(const uint32_t* rk, int nr, const uint8_t in[16],
                              uint8_t out[16]) {
  uint8_t s[16];
  memcpy(s, in, 16);
  addroundkey(s, rk);
  for (int r = 1; r < nr; r++) {
    subbytes(s);
    shiftrows(s);
    mixcolumns(s);
    addroundkey(s, rk + 4 * r);
  }
  subbytes(s);
  shiftrows(s);
  addroundkey(s, rk + 4 * nr);
  memcpy(out, s, 16);
}
void aes256ctr_init(aes256ctr_t* ctx, const unsigned char key[32],
                    const unsigned char noncectr[16]) {
  memset(ctx, 0, sizeof(*ctx));
  key_expansion(key, ctx->rk);
  ctx->nr = 14;
  memcpy(ctx->ctr, noncectr, 16);
}
static void ctr_inc(unsigned char ctr[16]) {
  for (int i = 15; i >= 0; --i) {
    ctr[i]++;
    if (ctr[i] != 0) break;
  }
}
void aes256ctr_xor(aes256ctr_t* ctx, unsigned char* buf, size_t len) {
  uint8_t ks[16];
  size_t off = 0;
  while (off < len) {
    aes_encrypt_block(ctx->rk, ctx->nr, ctx->ctr, ks);
    size_t take = (len - off) > 16 ? 16 : (len - off);
    for (size_t i = 0; i < take; i++) buf[off + i] ^= ks[i];
    off += take;
    ctr_inc(ctx->ctr);
  }
}
